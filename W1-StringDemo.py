best_class = input("What class is your favorite class: ")
# lets make our input completely capitalized
loud_version = best_class.upper()
# now lets make our input completely lowercase
lower_version = best_class.lower()
# you can also use ".title" to capitalize the first letter in each word.
print(f"Your favorite class is {loud_version}")
print(f"and the lower case version is {lower_version}")
